  D=M      // D = teclado (restaurar valor original)
  @32     // ASCII de tecla de espacio para borrar pantalla
  D=D-A     // D = tecla - 32
  @CLEAR_SCREEN
  D;JEQ     
  @LOOP
  0;JMP

