  @24576    // KBD
  D=M       // D = teclado (restaurar valor original)
  @76       // ASCII de 'L'
  D=D-A     // D = tecla - 76
  @DRAW_L
  D;JEQ     // si igual a 'L' -> saltar a dibujar L
  
  //hundir la tecla de espacio para limpiar pantalla 

             
