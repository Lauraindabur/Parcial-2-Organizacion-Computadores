(LOOP)
  @24576    // KBD
  D=M       // D = teclado
  @LOOP
  D;JEQ     // si D==0 -> no hay tecla, seguimos esperando

  @65       // ASCII de 'A'
  D=D-A     // D = tecla - 65
  @DRAW_A
  D;JEQ     // si igual a 'A' -> saltar a dibujar A
  
  // Si no es 'A', restaurar el valor original y verificar 'D'
  @24576    // KBD
  D=M       // D = teclado (restaurar valor original)
